/////////////////////////////////////////////////////////////////////////////////////////
//game functions INIT GAME
/////////////////////////////////////////////////////////////////////////////////////////
function InitGame()
{
    g_currentGameMode = new StateWorkbench();
    return;
}

// ///////////////////////////////////////////////////////////////////////////////////////
// game functions TICK GAME
// ///////////////////////////////////////////////////////////////////////////////////////
function TickGame(timeDelta)
{
    if (g_currentGameMode.Tick)
	g_currentGameMode.Tick(timeDelta);
}

// ///////////////////////////////////////////////////////////////////////////////////////
// game functions DRAW GAME
// ///////////////////////////////////////////////////////////////////////////////////////
function DrawGame()
{
    if (g_currentGameMode.Draw)
	g_currentGameMode.Draw();
}